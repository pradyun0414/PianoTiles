void Key::redrawKey(){


    if(oldY == y)
        return;
    else if(oldY < y){
        //__disable_irq();
        for(uint8_t i = oldY + 1; i <= y; i++){
            ST7735_DrawFastHLine(this->x, i, this->width, 0xFFFF);
        }
        //__enable_irq();
        oldY = this->y;
        ST7735_DrawBitmap(x, y + height, keyArray, width, height);

    }
    else if(oldY > y){
        for(int i = y; i < oldY; i++){
            ST7735_DrawFastHLine(this->x, i, this->width, 0xFFFF);
        }
        ST7735_DrawBitmap(x, y + height, keyArray, width, height);

    }




}

